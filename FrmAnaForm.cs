        public void listele()
        {
            try
            {

                SqlDataAdapter ad = new SqlDataAdapter("SELECT ogrid, ograd, ogrsoyad, ogrodano FROM Ogrenci", bgl.baglanti());
                DataSet ds = new DataSet();
                ad.Fill(ds);
                dataGridView1.DataSource = ds.Tables[0];

                // Kolon isimlerini doğru indekslerle ayarlayın
                dataGridView1.Columns[0].HeaderText = "ogrid";
                dataGridView1.Columns[1].HeaderText = "ograd";
                dataGridView1.Columns[2].HeaderText = "ogrsoyad";
                dataGridView1.Columns[3].HeaderText = "ogrodano";
                bgl.baglanti().Close();
            }

            catch (SqlException ex)
            {
                MessageBox.Show("HATA !!! " + ex.Message, "Hata", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }

        }
